import turtle
t = turtle.Turtle()
t.shape('turtle')

color_list = [ "azure", "CornflowerBlue", "blue", "DarkBlue", "DarkSlateBlue", "white"]


t.penup()
t.goto(-200,0)
t.pendown()
t.fillcolor(color_list[0])
t.begin_fill()
t.circle(100)
t.end_fill()
t.left(90)
t.penup()
t.forward(30)
t.pendown()
t.right(90)
t.fillcolor(color_list[5])
t.begin_fill()
t.circle(70)
t.end_fill()


t.penup()
